const { Client, LocalAuth } = require("whatsapp-web.js");
const logger = require("./logger");
const initCronJobs = require("./cronJobs");
const { autoGeneratedText } = require("./messageTemplates");
const client = new Client({
  // puppeteer: { product: "chrome", executablePath: "/usr/bin/chromium-browser" },
  restartOnAuthFail: true,
  puppeteer: {
    headless: true,
    args: [
      "--no-sandbox",
      "--disable-setuid-sandbox",
      "--disable-dev-shm-usage",
      "--disable-accelerated-2d-canvas",
      "--no-first-run",
      "--no-zygote",
      "--single-process", // <- this one doesn't works in Windows
      "--disable-gpu",
    ],
  },
  authStrategy: new LocalAuth(),
});

const createClient = () => {
  client.on("ready", () => {
    console.log(`Client Ready!`);
    client.sendMessage("923045032467@c.us", "Client started!");
    initCronJobs(client);
  });

  client.on("authenticated", () => {
    logger.info("AUTHENTICATED");
  });

  client.on("message", async (message) => {
    logger.info(`CHAT_ID: ${message.from}, AUTHOR: ${message.author}`);
    const messageText = message.body.toLowerCase();
    if (messageText.includes("eid") && messageText.includes("mubarak")) {
      const newMessage = "Khair Mubarak!\n" + autoGeneratedText;
      message.reply(newMessage);
    }
  });
  // client.on("ready", async () => {
  //   const me = (await client.getContacts()).filter((c) => c.isMe)[0];
  //   console.log(me);
  //   console.log(await me.getChat());
  //   client.sendMessage(me.id._serialized, "Hola!!");
  // });
  return client;
};

module.exports = createClient;
